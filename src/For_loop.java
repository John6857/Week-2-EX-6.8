
public class For_loop
{

	public static void main(String[] args)
	{
		// write a for loop to print the multiples of 3 from 300 down to 3.
		
		int number = 3;
		
		for (int i = 0; i <= 100; i++) {
		System.out.println(number + " * " + i + " = " + (number * i));
		}
	        
		  
		
		
		
		
		
		
		}

}
